    private void generateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateButtonActionPerformed
        String penyakit = this.generateData();
        boolean isSehat = false;
        
        if(this.penyakit[0] == 0 && this.penyakit[1] == 0 && this.penyakit[2] == 0 && this.penyakit[3] == 0 && this.penyakit[4] == 0)
            isSehat = true;
        if(isSehat) {
            HasilDiagnosa hasilDiagnosa = new HasilDiagnosa(this.kode_pengguna);
            hasilDiagnosa.setLocation(this.getLocation());
            hasilDiagnosa.setVisible(true);
            this.dispose();
        }
        else {
            HasilDiagnosa hasilDiagnosa = new HasilDiagnosa(penyakit, this.kode_pengguna);
            hasilDiagnosa.setLocation(this.getLocation());
            hasilDiagnosa.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_generateButtonActionPerformed

