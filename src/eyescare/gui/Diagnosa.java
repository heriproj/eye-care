    private void mundurButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mundurButtonActionPerformed
        this.backSistem();
        this.indikator--;
        System.out.println(this.indikator);
        if(this.indikator < 1)
            this.mundurButton.setEnabled(false);
        else
            this.mundurButton.setEnabled(true);
        if(this.indikator > 10) {
            this.lanjutButton.setEnabled(false);
            this.generateButton.setEnabled(true);
        }
        else {
            this.lanjutButton.setEnabled(true);
            this.generateButton.setEnabled(false);
        }
        this.diagnosaLabel.setText(pernyataan[indikator]);
        if(this.yaRadioButton.isSelected())
            this.yaRadioButton.setSelected(false);
        if(this.tidakRadioButton.isSelected())
            this.tidakRadioButton.setSelected(false);
    }//GEN-LAST:event_mundurButtonActionPerformed

